#include "ft_engine.h"

int     ft_getcolor(int i)
{
    int j;

    if (i < 0)
        return (OFFSET + 5);
    j = 0;
    while (i > 1 && ++j)
            i /= 2;
    j += OFFSET + 6;
    return ((j > (OFFSET + 17)) ? (OFFSET + 17) : j);
}
